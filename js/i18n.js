// ── Aktif uygulama dili ──
let APP_LANG = 'tr';

// ── Çeviri fonksiyonu ──
function t(key) {
  return UI_TRANSLATIONS[APP_LANG]?.[key] ?? UI_TRANSLATIONS['en']?.[key] ?? key;
}

// ── Dil değiştir ──
function setAppLanguage(lang) {
  if (!SUPPORTED_UI_LANGS.includes(lang)) lang = 'en';
  APP_LANG = lang;
  localStorage.setItem('app_lang', lang);
  applyTranslations();
  const sel = document.getElementById('app-language');
  if (sel) sel.value = APP_LANG;
}

// ── Çevirileri uygula ──
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);
    if (translation) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translation;
      } else {
        el.textContent = translation;
      }
    }
  });
}

// ── Dil algılama ──
function detectAppLang() {
  const saved = localStorage.getItem('app_lang');
  if (saved && SUPPORTED_UI_LANGS.includes(saved)) return saved;
  const browserLang = navigator.language.split('-')[0];
  return SUPPORTED_UI_LANGS.includes(browserLang) ? browserLang : 'en';
}

// ── Uygulama dilini başlat ──
function initAppLanguage() {
  APP_LANG = detectAppLang();
  applyTranslations();
  const sel = document.getElementById('app-language');
  if (sel) sel.value = APP_LANG;
}

// ── Öğrenilen dil tercihini değiştir ──
function setLearnLangFromSettings(langCode) {
  if (!CU) return;
  CU.learnLang = langCode;
  G.learnLang = langCode;
  saveDB(DB);
  alert(`Öğrenilen dil ${SUPPORTED_TARGET_LANGS[langCode]?.name || langCode} olarak ayarlandı.`);
}

// ── Global export (diğer dosyalardan erişim için) ──
window.APP_LANG = APP_LANG;
window.t = t;
window.setAppLanguage = setAppLanguage;
window.applyTranslations = applyTranslations;
window.initAppLanguage = initAppLanguage;
window.setLearnLangFromSettings = setLearnLangFromSettings;
window.SUPPORTED_UI_LANGS = SUPPORTED_UI_LANGS;
window.SUPPORTED_TARGET_LANGS = SUPPORTED_TARGET_LANGS;